clear;
thk = [1.3691 2.9199 3.8423 4.9508 5.6716 5.9603]';
rk = [1.4711 4.7958 3.4448 1.4814 0.8280 0.8312]';
H =  [thk.^0 -rk.*cos(thk)];
b =  rk;
%C = H\b;
C=H([1:3],:)\b([1:3],:);
TH=[0:0.01:2*pi];
r=C(1)./(1+C(2).*cos(TH));
xx=r.*cos(TH);
yy=r.*sin(TH);
%xx2=rk.*cos(thk);
%yy2=rk.*sin(thk);
xx2=rk([1:3],:).*cos(thk([1:3],:));
yy2=rk([1:3],:).*sin(thk([1:3],:));
plot(xx2,yy2,'bo',xx,yy,'r');